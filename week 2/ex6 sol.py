x = str(input())



for ch in x:
    if(ch=='a'):
        print('ka',end='')
    elif(ch=='e'):
        print('ke',end='')
    elif (ch == 'i'):
        print('ki', end='')
    elif (ch == 'o'):
        print('ko', end='')
    elif (ch == 'u'):
        print('ku', end='')
    elif (ch == 'y'):
        print('ky', end='')
    else:
        print(ch+'i',end='')